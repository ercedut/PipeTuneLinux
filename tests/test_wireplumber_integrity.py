    rule_dir, manifest_path = _isolated(tmp_path)
    preview = _make_preview(tmp_path)
    install_report = _install(tmp_path, preview, rule_dir, manifest_path)
    run_rollback_rule(install_report.install_id, dry_run=False, confirm_rollback=True, manifest_path=manifest_path)

    entries_before = load_manifest(manifest_path)
