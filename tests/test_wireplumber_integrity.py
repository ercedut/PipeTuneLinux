def test_state_doctor_clean_pass(tmp_path: Path) -> None:
    rule_dir, manifest_path = _isolated(tmp_path)
    preview = _make_preview(tmp_path)
    _install(tmp_path, preview, rule_dir, manifest_path)

    report = run_state_doctor(manifest_path=manifest_path, rule_dir=rule_dir)

    assert report.verdict == "pass"
    assert report.active_count == 1
    assert report.missing_file_count == 0
    assert report.orphan_file_count == 0
    assert report.checksum_mismatch_count == 0


