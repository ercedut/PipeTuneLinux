def test_install_succeeds_with_ready_preflight_and_confirmation(monkeypatch, tmp_path: Path) -> None:
    monkeypatch.setenv("PIPETUNE_HOME", str(tmp_path / "home"))
    monkeypatch.setattr("pipetune.activation.installer.run_profile_preflight", lambda _config: _preflight("ready"))
    config = _write_config_with_manifest(tmp_path)

    result = install_profile(config, user_level=True, confirm_install=True, confirm_hardware_quirk=False)

    assert result.success is True
    assert result.destination_path is not None
    installed = Path(result.destination_path)
    assert installed.exists()
    assert installed.read_text(encoding="utf-8") == config.read_text(encoding="utf-8")
    assert result.manifest_path is not None and Path(result.manifest_path).exists()
    manifest = list_install_manifests()[0][1]
    assert manifest.source_sha256 == sha256_file(config)
    assert manifest.installed_sha256 == sha256_file(installed)


def test_install_cli_does_not_restart_services(monkeypatch, tmp_path: Path, capsys) -> None:
    monkeypatch.setenv("PIPETUNE_HOME", str(tmp_path / "home"))
    monkeypatch.setattr("pipetune.activation.installer.run_profile_preflight", lambda _config: _preflight("ready"))
    config = _write_config_with_manifest(tmp_path)

    assert cli.main(["profile", "install", str(config), "--user", "--confirm-install"]) == 0
    output = capsys.readouterr().out

    assert "No services were restarted automatically." in output
    assert "systemctl --user restart pipewire pipewire-pulse wireplumber" in output


def test_install_writes_only_user_level_destination(monkeypatch, tmp_path: Path) -> None:
    monkeypatch.setenv("PIPETUNE_HOME", str(tmp_path / "home"))
    monkeypatch.setattr("pipetune.activation.installer.run_profile_preflight", lambda _config: _preflight("ready"))
    config = _write_config_with_manifest(tmp_path)

    result = install_profile(config, user_level=True, confirm_install=True, confirm_hardware_quirk=False)

    assert result.destination_path is not None
    assert Path(result.destination_path).parent == user_pipewire_config_dir()
